'''
A program that reads the base and height of a parallelogram and displays
its area. (The area of a parallelogram is calculated as **base Ã— height**.)
'''

base = float(input("Enter the base of the parallelogram: "))
height = float(input("Enter the height of the parallelogram: "))
area = base * height
print('The area of the parallelogram is', area)